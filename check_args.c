#include "philo.h"

int	check_args(int ac, char **av)
{
    int	i;
    int	j;

    i = 1;
    while (ac > i)
    {
		j = 0;
		while (av[i][j])
		{
			if (av[i][j] == '+')
				j++;
			if (av[i][j] < '0' || av[i][j] > '9')
				return (0);
			j++;
		}
		i++;
    }
    return (1);
}